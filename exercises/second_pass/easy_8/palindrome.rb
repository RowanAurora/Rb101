=begin
Write a method that returns a list of all substrings of a string that are palindromic. That is, each substring must consist of the same sequence of characters forwards as it does backwards. The return value should be arranged in the same sequence as the substrings appear in the string. Duplicate palindromes should be included multiple times.

You may (and should) use the substrings method you wrote in the previous exercise.

For the purposes of this exercise, you should consider all characters and pay attention to case; that is, "AbcbA" is a palindrome, but neither "Abcba" nor "Abc-bA" are. In addition, assume that single characters are not palindromes.
PROBLEM:

Write a method that returns a list of all substrings of a string that are palindromic. should be ordered by appearence. duplicates should be there.
Examples:

palindromes('abcd') == []
palindromes('madam') == ['madam', 'ada']
palindromes('hello-madam-did-madam-goodbye') == [
  'll', '-madam-', '-madam-did-madam-', 'madam', 'madam-did-madam', 'ada',
  'adam-did-mada', 'dam-did-mad', 'am-did-ma', 'm-did-m', '-did-', 'did',
  '-madam-', 'madam', 'ada', 'oo'
]
palindromes('knitting cassettes') == [
  'nittin', 'itti', 'tt', 'ss', 'settes', 'ette', 'tt'
]

=end

def palindromes(string)
  result = []
  start_count = 0
  loop do
    end_count = 1
    loop do 
      check = string[start_count, end_count]
      if check == check.reverse && check.size >= 2
        result << string[start_count, end_count]
      end
    break if end_count == (string.size) - start_count
    end_count += 1
    end
  start_count += 1
  break if start_count == string.size
  end
  result
end

